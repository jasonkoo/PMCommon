package com.lenovo.lps.push.marketing.common.exception;

public class NotFoundException extends Exception {
	private static final long serialVersionUID = 802322124085032649L;

	public NotFoundException() {
	}

	public NotFoundException(String arg0) {
		super(arg0);
	}

	public NotFoundException(Throwable arg0) {
		super(arg0);
	}

	public NotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
